package network

import "net"

//ListAddress list all available IP address in this machine
func ListAddress() ([]string, error) {
	result := make([]string, 0, 255)
	ifaces, err := net.Interfaces()
	if err != nil {
		return nil, err
	}
	for _, iface := range ifaces {
		if iface.Flags&net.FlagUp == 0 {
			continue // interface down
		}
		addrs, err := iface.Addrs()
		if err != nil {
			return nil, err
		}
		for _, addr := range addrs {
			var ip net.IP
			switch v := addr.(type) {
			case *net.IPNet:
				ip = v.IP
			case *net.IPAddr:
				ip = v.IP
			}
			if ipAddr := ip.String(); ipAddr == "" || ipAddr == "<nil>" {
				continue
			} else {
				result = append(result, ipAddr)
			}
		}
	}
	return result, nil
}
